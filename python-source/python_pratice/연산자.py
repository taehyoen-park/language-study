num1 = 10
num2 = 45
#더하기
print(2+2)

#빼기
print(2-3)
#나누기
print(4//2)
#모둘러
print(5%2) 
#곱하기
print(4*3)
#나누기(소수점 있음)
print(7/2) 
#제곱
print(3**2)
#우선순위 * -> // -> + -> -
print(num1+num2*4//2+num1)

# &,and 둘중 하나라도 거짓이면 거짓이다
print(1 and 0)
print(1 & 1)

# |,or 둘중 하나가 참이면 참이다 True(1),False(0)
print(1 or 0)
print(0 | 0)

# not 특정값에 반대값을 돌려준다
print(not(False))
print((True))

# == 같음을 확인하는 연산자
print(3 == 5)
print(3 == 3)

# != 같지않음을 확인하는 연산자
print(2 != 3)
print(2 != 2)

# is 두개의 객체가 완전히 동일한것인지 확인한다
print(1 is 1.0)
print(type(1)) 
print(type(1.0))

# is not 동일한것이 아닌지 비교
print(1 is not 1.0)

# <,>,<=,>= 대소비교
print(3 > 4)
print(9 < 10)













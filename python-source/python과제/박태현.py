import turtle
win = turtle.Screen()
w=1000
h=500
offset =100
win.setup(w,h)
t=turtle.Turtle('circle')
size = int(input('글자 크기:'))
t.speed(30)

#'박'그리기
t.pencolor('blue')
t.pensize(10)
t.penup()
t.goto(-w/2,h/2-offset)
t.pendown()
t.setheading(270)
t.forward(size)
t.left(90)
t.forward(size)
t.left(90)
t.forward(size)
t.penup()
t.backward(size/2)
t.setheading(180)
t.pendown()
t.forward(size)
t.penup()
t.setheading(0)
t.forward(size+size/4)
t.setheading(90)
t.pendown()
t.forward(size)
t.backward(size*2)
t.forward(size)
t.setheading(0)
t.forward(size)
t.penup()
t.goto(-w/2,h/2-offset)
t.setheading(270)
t.forward(size+size/4)
t.setheading(0)
t.pendown()
t.forward(size)
t.right(90)
t.forward(size)

#'태'만들기
t.pencolor('yellow')
t.pensize(15)
t.penup()
t.goto(-w/3,h/2-offset)
t.pendown()
t.setheading(0)
t.forward(size)
t.backward(size)
t.setheading(270)
t.forward(size)
t.left(90)
t.forward(size)
t.backward(size)
t.right(90)
t.backward(size/2)
t.setheading(0)
t.forward(size)
t.penup()
t.forward(size/4)
t.setheading(90)
t.pendown()
t.forward(size)
t.backward(2*size)
t.forward(size)
t.setheading(0)
t.forward(size)
t.setheading(270)
t.forward(size)
t.backward(2*size)

#'현'만들기
t.pencolor('green')
t.pensize(10)
t.penup()
t.goto(-w/6,h/2-offset)
t.pendown()
t.setheading(0)
t.forward(size)
t.penup()
t.backward(size/2)
t.setheading(90)
t.forward(size/6)
t.setheading(0)
t.pendown()
t.forward(size/6)
t.backward(size/3)
t.penup()
t.goto(-w/6,h/2-offset)
t.setheading(0)
t.forward(size/2)
t.setheading(270)
t.forward(size)
t.setheading(0)
t.backward(size/4)
t.pendown()
t.circle(size/3)
t.penup()
t.setheading(0)
t.forward(size/1.5)
t.setheading(90)
t.forward(size/4)
t.setheading(0)
t.pendown()
t.forward(size/2)
t.setheading(90)
t.forward(size/2)
t.backward(size/2+size/4)
t.setheading(180)
t.forward(size/2)
t.backward(size/2)
t.setheading(270)
t.forward(4*size/5)
t.penup()
t.forward(size/2)
t.setheading(180)
t.pendown()
t.forward(size)
t.setheading(90)
t.forward(size)
